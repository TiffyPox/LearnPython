def main():
    print("Hello World")

if __name__ == "__main__":
    main()

x = ("Princess Peach")

# returning a part of a string using slicing
print(x[0:6]) # from index 0 to index 6

# alternatively
print(x[:6])
print(x[4:])

# you can also use negative indexing to return the values
print(x[-5:])

